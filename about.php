                        <p class="text">
                        (Isaiah 2:1-4 says.) <br>
                        The vision that Isaiah the son of Amoz saw concerning the government of God on earth. He saw that in the last days the government of God which he called the mountain of God shall be established above other kingdoms thus mountains and exalted above them; and all the nations shall come and worship there to learn God’s ways and walk in His paths. There shall be no wars for nations will be at peace with one another.
                        </p><p>Mountain represents government! Christ will take over the government of our troubled world. He will rule as Supreme head over all the governors and kings of this world. He will rule them with a rod of iron – authority and power. And of the increase of His government and peace they will be no end. He will enforce the commandments and laws of God’s government over all nations. All the nations of the world will come under Elohim government. This He will save humanity from its foolishness! </p>
<p>
Prophet Isaiah prophesied that unto us a child shall be born and the government shall be upon His shoulder. And His name will be called wonderful, Counsellor, Mighty God, Everlasting father, Prince of Peace, of the increase of his government and peace there will be no end (Isaiah 9: 6&7).  </p>
<p>
Micah 4:1-3 also describes the soon coming literal government: “Now it shall come to pass in the latter days that the mountain of the LORD’s house shall be established on the top of the mountains, and shall be exalted above the hills; and peoples shall flow to it. Many nations shall come and say, ‘Come, and let us go up to the mountain of the LORD, to the house of the God of Jacob; He will teach us His ways, and we shall walk in His paths. For out of Zion the law shall go forth, and the word of the LORD from Jerusalem. He shall judge between many peoples, and rebuke strong nations afar off…’’ </p>
<p>
This passage shows that Yashua Messiah will dwell on earth in Jerusalem, accessible to physical human beings and nations in the earth. </p>
<p>
Jesus Christ will take over the government of this present world and enforce peace on earth. He will rule with authority which Revelation 19: 15 refers to as ‘‘rod of iron.’’ He will rule and reign over the nations for a thousand years called the ‘’millennium.’’ Justice and judgement will be the distinctive characteristics of His government. It means that oppression and corruption will become a thing of the past. </p>

<p>
Zachariah 14:9 also confirms that Christ ruling will be on earth not in heaven: ‘’ And the shall be king over all the earth: in that day shall there be one Lord, and his name is one.’ </p>
<p>’
Daniel 7:1-28 vision confirms this!</p>
<p>
This chapter treats of the same subject as the second chapter. In the second chapter the images are taken from the inanimate sphere; in the seventh chapter they are taken from the animate. Nebuchadnezzar saw superficially the world power as a splendid human figure and the kingdom of God as a mere stone at the first. Daniel sees the world kingdoms in their inner essence as of an animal nature lower than human, being estranged from God; and that only in the kingdom of God - Christ - is the true dignity of man realized. So, as contrasted with Nebuchadnezzar's vision, the kingdom of God appears to Daniel, from the very first, superior to the world kingdom. </p>
<p>
Nebuchadnezzar's colossal image represents mankind in its own strength, but only the outward man. Daniel sees man spiritually degraded to the beast level, led by blind impulses, through his alienation from God. Humanity without God’s law guiding them is blind, they have no future. It is only under the government of God can man fulfil his purpose. The true destiny of man is with and in God via submission to His rule of law. </p>
<p>
But it has not yet occurred to humanity that without divinity they are impossible. Man without God decay and deteriorate to beast level. There will be no future for earth if God does not intervene by enforcing the rule of His government on humanity. The leaders of world empires are compared to "bulls" in Psalms 68:30. What makes man valuable is his communion with God, in willing subjection to Him. The moment he tries to exalt himself to independence of God, as did Nebuchadnezzar (Daniel 4:30), he sinks to the beast's level. This is where humanity is headed – bestiality - if Christ does not return quickly! We must pray for the government of God to come! </p>
<p>
Through Nebuchadnezzar God revealed how the end time four ruling governments will succeed one another. The prophet Daniel was called before the king to interpret the vision he had in form of a dream when he was meditating on the future destiny of his vast empire. Daniel 2:31–34:
</p>
<p>
‘’You, O king, were watching; and behold, a great image! This great image, whose splendour was excellent, stood before you; and its form was awesome. This image’s head was of fine gold, its chest and arms of silver, its belly and thighs of bronze, its legs of iron, its feet partly of iron and partly of clay. You watched while a stone was cut out without hands, which struck the image on its feet of iron and clay, and broke them in pieces.’’ </p>
<p>
Daniel told the king that the head of gold represented Nebuchadnezzar himself and his empire of Babylon. This empire was replaced by the Medo-Persian Empire, represented by the chest and arms of silver. The belly and thighs of bronze represented the Greco-Macedonian Empire of Alexander the Great, and the two legs of Iron represent the Roman Empire. Finally, the ten toes on two feet of iron mixed with ceramic clay represent a future revival of the Roman Empire. </p>
<p>
Bible prophecy reveals that a great superpower will arise in Europe, and it will be a revival of the Roman Empire.  </p>
<p>
After that, the rock which represents the government of God will smash the image which represents the governments of this world to bits and take over all the governments of the world. </p>
<p>
This superpower government that will rule worldwide is building up to collapse. Bible recorded that the revived Roman Empire will be led by a powerful leader called “the beast. The ten kings seen in Revelation 17 symbolized by horns on the beast, will unify and give their power and authority to the beast”. </p>
<p>
This final end-time one world-ruling Beast will come to deceive many by a counterfeit power and a promise of perfect governance. As Revelation 13:5–8 declares. He will show himself as God</p>
<p>
The Apostle Paul warns of a great false prophet who will stand in the holy place. “Let no one deceive you by any means; for that Day will not come unless the falling away comes first, and the man of sin is revealed, the son of perdition, who opposes and exalts himself above all that is called God or that is worshiped, so that he sits as God in the temple of God, showing himself that he is God” (2 Thessalonians 2:3–4).
</p>
<p>
But the good news is Elohim government – the kingdom of God! </p>
<p>
Christ came to make an announcement about a total change in the manner in which the world would one day be governed. With this change, would come supernatural world peace, joy, general wellbeing.  </p>
<p>
The new life and pattern of living was the subject of His message. When He mobilized His twelve disciples and sent them to teach, the guideline was to teach about the government of God (Luke 9:1-2). When He later sent His seventy followers (Luke 10:1), He also instructed them to teach the government of God.	</p>
<p>
This is the greatest news ever that humanity will one day be free from all the torments and pain this present world and government brings. This is the hope we have knowing that this world is not our permanent home.  </p>

